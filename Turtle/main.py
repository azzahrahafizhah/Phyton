from turtle import *

getscreen()
bgcolor("pink")

speed(1)

penup() #untuk mengangkat pena
forward(100) #untuk maju
pendown() #gak ada pendown, dia bakal ngangkat terus

forward(100)
left(90)
forward(100)
right(90)
forward(100)

pencolor("purple") # untuk merubah warna pen (garis)
pensize(20) # untuk mengubah ukuran pen (garis)

shape("turtle")

done()

